t = int(input())
for _ in range(t):
    n = float(input())
    n = n/100000
    tax=0
    if n <= 2.5:
        tax = 0
    elif n > 2.5 and n <= 5:
        tax = (n-2.5)*5/100
    elif n > 5 and n <=7.5:
        tax = (5-2.5)*5/100 + (n-5)/10
    elif n > 7.5 and n<=10:
        tax = 2.5*5/100+(7.5-5)/10+(n-7.5)*15/100
    elif n > 10 and n<=12.5:
        tax = 2.5*5/100+(5-7.5)/10+(10-7.5)*15/100+(n-10)/5
    elif n>12.5 and n<=15:
        tax = 2.5*5/100+(5-7.5)/10+(10-7.5)*15/100 + (12.5-10)/5+(n-12.5)/4
    else:
        tax = 2.5*5/100+(5-7.5)/10+(10-7.5)*15/100 + (12.5-10)/5+ (15-12.5)/4+(n-15)*30/100
    
    print(int((n-tax)*100000))
            
            
